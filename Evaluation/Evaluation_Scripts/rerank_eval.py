# Invoked as:
# CUDA_VISIBLE_DEVICES=0,1 python rerank_eval.py "CodeShield/Themis-RM-32B" --dataset "CodeShield/ccplus-rerank" --config "python" --output results_py --use-system-prompt --batch-size 8 --max-length 4096

"""
Re-ranking metric evaluation for scalar reward models on code-completion datasets.

Given a dataset where each row is a coding question with multiple completions and
test-case execution results, this script:
  1. Scores every completion with the reward model.
  2. Reports three families of metrics (per-question, then aggregated):
     a) Reward variance per question.
     b) Hits@K (K=5, 10): whether a passing completion appears in the top-K by reward.
     c) Rank correlation (Spearman) between test-case pass-rate and reward scores.
"""

import torch
from transformers import AutoConfig, AutoTokenizer, AutoModelForSequenceClassification
from datasets import load_dataset
import pandas as pd
import numpy as np
from scipy.stats import spearmanr
from tqdm import tqdm
import argparse
import json
import os


# ---------------------------------------------------------------------------
# System prompt (Functional Correctness only)
# ---------------------------------------------------------------------------
FUNCTIONAL_CORRECTNESS_SYSTEM_PROMPT = (
    "You are an experienced AI judge and evaluator, specifically designed and "
    "trained to assess the overall quality of responses to human prompts. The "
    "prompts can be of various types, including but not limited to open-ended "
    "questions, coding tasks, creative writing, requests for information, and "
    "more. The responses may be generated by a language model or a human and "
    "may include text, code, or both. You will receive the prompt and the "
    "generated response for each judgement session. Your exact mandate is to "
    "score the response based on a pre-specified but flexible set of criteria.\n\n"
    "The following criteria must govern your scoring for the current judgment "
    "session:\n\n"
    "1. **Helpfulness**: How well does the response address the prompt? Does it "
    "provide a complete and thorough answer, or is it lacking in essential "
    "details? This is a tenet that applies to evaluating all types of responses. "
    "You must score helpful responses higher than less unhelpful ones.\n\n"
    "2. **Harmlessness**: Does the response contain any harmful or inappropriate "
    "content? This includes hate speech, violence, self-harm, and other forms of "
    "harmful content. This is a tenet that applies to evaluating all types of "
    "responses. You must score harmless responses higher than harmful ones.\n\n"
    "3. **Functional Correctness**: Does the response follow best practices for "
    "functional correctness? Examples include correct implementation of "
    "algorithms, adherence to specifications, and proper handling of edge cases, "
    "among others. This is a tenet that applies to evaluating code responses. "
    "You must score more functionally correct responses higher than less "
    "functionally correct ones."
)


# ---------------------------------------------------------------------------
# Model wrapper
# ---------------------------------------------------------------------------
class RewardModelScorer:
    """Loads a sequence-classification reward model and scores conversations."""

    def __init__(self, model_path: str, max_length: int, use_system_prompt: bool):
        self.use_system_prompt = use_system_prompt
        print(f"System prompt enabled: {use_system_prompt}")

        print(f"Loading model and tokenizer from: {model_path}")
        self.tokenizer = AutoTokenizer.from_pretrained(model_path)
        config = AutoConfig.from_pretrained(model_path, trust_remote_code=True)
        config.pad_token_id = self.tokenizer.pad_token_id

        device_map = "auto" if torch.cuda.is_available() else "cpu"
        if torch.cuda.is_available():
            print(f"CUDA detected: {torch.cuda.device_count()} GPU(s)")
        else:
            print("Using CPU inference")

        self.model = AutoModelForSequenceClassification.from_pretrained(
            model_path,
            config=config,
            torch_dtype=torch.float32,
            device_map=device_map,
            trust_remote_code=True,
        )
        self.model.eval()
        self.input_device = next(iter(self.model.parameters())).device.type

        # Resolve max length
        tok_max = getattr(self.tokenizer, "model_max_length", None)
        if tok_max is not None and tok_max < max_length:
            self.max_length = tok_max
            print(f"Warning: tokenizer max length ({tok_max}) < requested ({max_length}). Using {tok_max}.")
        else:
            self.max_length = max_length
        print(f"Max sequence length: {self.max_length}")

        if self.tokenizer.pad_token is None:
            self.tokenizer.pad_token = self.tokenizer.eos_token

    # ---- formatting --------------------------------------------------------
    def format_conversation(self, prompt: str, completion: str) -> str:
        conversation = []
        if self.use_system_prompt:
            conversation.append({"role": "system", "content": FUNCTIONAL_CORRECTNESS_SYSTEM_PROMPT})
        conversation.extend([
            {"role": "user", "content": prompt},
            {"role": "assistant", "content": completion},
        ])
        if hasattr(self.tokenizer, "apply_chat_template") and self.tokenizer.chat_template is not None:
            try:
                return self.tokenizer.apply_chat_template(
                    conversation, tokenize=False, add_generation_prompt=False
                )
            except Exception as e:
                print(f"Warning: chat template failed ({e}), using fallback")
        # fallback
        if self.use_system_prompt:
            return (
                f"System:\n{FUNCTIONAL_CORRECTNESS_SYSTEM_PROMPT}\n\n"
                f"User:\n{prompt}\n\nAssistant:\n{completion}"
            )
        return f"User:\n{prompt}\n\nAssistant:\n{completion}"

    # ---- batched scoring ---------------------------------------------------
    @torch.inference_mode()
    def score_texts(self, texts: list[str], batch_size: int) -> list[float]:
        """Score a flat list of formatted conversation strings."""
        all_scores: list[float] = []
        for i in tqdm(range(0, len(texts), batch_size), desc="Scoring completions"):
            batch = texts[i : i + batch_size]
            inputs = self.tokenizer(
                batch,
                return_tensors="pt",
                truncation=True,
                max_length=self.max_length,
                padding=True,
            ).to(self.input_device)
            logits = self.model(**inputs).logits.squeeze(-1).detach()
            if logits.dim() == 0:
                logits = logits.unsqueeze(0)
            all_scores.extend(logits.cpu().tolist())
            del inputs, logits
            if torch.cuda.is_available():
                torch.cuda.empty_cache()
        return all_scores


# ---------------------------------------------------------------------------
# Metric helpers
# ---------------------------------------------------------------------------

def compute_hits_at_k(rewards: np.ndarray, passed: np.ndarray, k: int) -> float:
    """Return 1.0 if any of the top-k scored completions passed all tests."""
    if k > len(rewards):
        k = len(rewards)
    top_k_indices = np.argsort(rewards)[::-1][:k]
    return float(passed[top_k_indices].any())


def compute_spearman(rewards: np.ndarray, pass_rates: np.ndarray) -> float:
    """Spearman rank correlation; returns NaN when undefined."""
    if len(rewards) < 2 or np.std(rewards) == 0 or np.std(pass_rates) == 0:
        return float("nan")
    result = spearmanr(rewards, pass_rates)
    corr = result.statistic
    # In newer scipy, .statistic can be a 2x2 matrix for two 1-D inputs
    if hasattr(corr, 'shape') and corr.shape:
        corr = corr[0, 1]
    return float(corr)


# ---------------------------------------------------------------------------
# Main evaluation loop
# ---------------------------------------------------------------------------

def evaluate(
    scorer: RewardModelScorer,
    dataset_name: str,
    config: str,
    split: str,
    batch_size: int,
):
    """
    Score every completion in the dataset and compute metrics.

    Because different questions have different numbers of completions, we
    flatten all (prompt, completion) pairs into one list, score them in
    contiguous batches for GPU efficiency, then un-flatten using the recorded
    lengths.
    """
    print(f"Loading dataset: {dataset_name} (config={config}, split={split})")
    dataset = load_dataset(dataset_name, config, split=split)
    print(f"Loaded {len(dataset)} questions")

    # ------------------------------------------------------------------
    # 1. Flatten all completions into a single list for efficient batching
    # ------------------------------------------------------------------
    flat_texts: list[str] = []
    lengths: list[int] = []          # number of completions per question
    meta: list[dict] = []            # per-question metadata

    for row in tqdm(dataset, desc="Formatting completions"):
        prompt = row["description"]
        completions = row["completions"]
        n = len(completions)
        lengths.append(n)
        meta.append({
            "id": row["id"],
            "language": row.get("language", config),
            "num_passed": row["num_passed"],
            "num_failed": row["num_failed"],
            "final_verdict": row["final_verdict"],
        })
        for comp in completions:
            flat_texts.append(scorer.format_conversation(prompt, comp))

    total_completions = len(flat_texts)
    print(f"Total completions to score: {total_completions}")

    # ------------------------------------------------------------------
    # 2. Score everything in batches
    # ------------------------------------------------------------------
    flat_scores = scorer.score_texts(flat_texts, batch_size)

    # ------------------------------------------------------------------
    # 3. Un-flatten and compute per-question metrics
    # ------------------------------------------------------------------
    per_question: list[dict] = []
    offset = 0
    for length, m in zip(lengths, meta):
        rewards = np.array(flat_scores[offset : offset + length])
        num_passed = np.array(m["num_passed"])
        num_failed = np.array(m["num_failed"])
        final_verdict = np.array(m["final_verdict"])
        total_tests = num_passed + num_failed
        pass_rate = np.where(total_tests > 0, num_passed / total_tests, 0.0)

        has_positive = final_verdict.any()

        row_metrics = {
            "id": m["id"],
            "language": m["language"],
            "n_completions": length,
            "n_passing": int(final_verdict.sum()),
            "reward_mean": float(rewards.mean()),
            "reward_std": float(rewards.std()),
            "reward_variance": float(rewards.var()),
            "hits_at_5": compute_hits_at_k(rewards, final_verdict, 5) if has_positive else float("nan"),
            "hits_at_10": compute_hits_at_k(rewards, final_verdict, 10) if has_positive else float("nan"),
            "spearman": compute_spearman(rewards, pass_rate),
            "rewards": rewards.tolist(),
        }
        per_question.append(row_metrics)
        offset += length

    df = pd.DataFrame(per_question)

    # ------------------------------------------------------------------
    # 4. Aggregate
    # ------------------------------------------------------------------
    # For hits@K we only consider questions that have at least one passing completion
    df_with_pos = df[df["n_passing"] > 0]

    agg = {
        "n_questions": len(df),
        "n_questions_with_positive": len(df_with_pos),
        "total_completions": total_completions,
        "reward_variance_mean": round(float(df["reward_variance"].mean()), 6),
        "reward_variance_median": round(float(df["reward_variance"].median()), 6),
        "reward_variance_std": round(float(df["reward_variance"].std()), 6),
    }

    if len(df_with_pos) > 0:
        agg["hits_at_5"] = round(float(df_with_pos["hits_at_5"].mean()), 4)
        agg["hits_at_10"] = round(float(df_with_pos["hits_at_10"].mean()), 4)
    else:
        agg["hits_at_5"] = float("nan")
        agg["hits_at_10"] = float("nan")

    spearman_valid = df["spearman"].dropna()
    agg["spearman_mean"] = round(float(spearman_valid.mean()), 4) if len(spearman_valid) else float("nan")
    agg["spearman_median"] = round(float(spearman_valid.median()), 4) if len(spearman_valid) else float("nan")

    return {
        "overall": agg,
        "per_question": per_question,
    }


# ---------------------------------------------------------------------------
# I/O helpers
# ---------------------------------------------------------------------------

def print_results(results: dict):
    overall = results["overall"]
    print("\n" + "=" * 65)
    print("RE-RANKING EVALUATION RESULTS")
    print("=" * 65)
    print(f"\n  Questions evaluated:        {overall['n_questions']}")
    print(f"  Questions with ≥1 positive: {overall['n_questions_with_positive']}")
    print(f"  Total completions scored:   {overall['total_completions']}")
    print(f"\n  Reward variance (mean):     {overall['reward_variance_mean']:.6f}")
    print(f"  Reward variance (median):   {overall['reward_variance_median']:.6f}")
    print(f"  Reward variance (std):      {overall['reward_variance_std']:.6f}")
    print(f"\n  Hits@5:                     {overall['hits_at_5']:.4f}")
    print(f"  Hits@10:                    {overall['hits_at_10']:.4f}")
    print(f"\n  Spearman corr (mean):       {overall['spearman_mean']:.4f}")
    print(f"  Spearman corr (median):     {overall['spearman_median']:.4f}")
    print("=" * 65)


def save_results(results: dict, output_dir: str):
    os.makedirs(output_dir, exist_ok=True)

    # Per-question detail as parquet (drop the raw reward lists for the parquet)
    detail_rows = [
        {k: v for k, v in row.items() if k != "rewards"}
        for row in results["per_question"]
    ]
    detail_df = pd.DataFrame(detail_rows).set_index("id")
    parquet_path = os.path.join(output_dir, "per_question_scores.parquet")
    detail_df.to_parquet(parquet_path, index=True)
    print(f"Per-question scores saved to: {parquet_path}")

    # Full rewards as separate parquet for downstream analysis
    rewards_rows = [
        {"id": row["id"], "rewards": row["rewards"]}
        for row in results["per_question"]
    ]
    rewards_df = pd.DataFrame(rewards_rows).set_index("id")
    rewards_path = os.path.join(output_dir, "rewards.parquet")
    rewards_df.to_parquet(rewards_path, index=True)
    print(f"Raw rewards saved to: {rewards_path}")

    # Summary JSON (aggregate only)
    json_path = os.path.join(output_dir, "results.json")
    summary = {"overall": results["overall"]}
    with open(json_path, "w") as f:
        json.dump(summary, f, indent=2)
    print(f"Summary results saved to: {json_path}")


# ---------------------------------------------------------------------------
# CLI
# ---------------------------------------------------------------------------

def main():
    parser = argparse.ArgumentParser(
        description="Re-ranking evaluation of a reward model on a code-completion dataset"
    )
    parser.add_argument("model_path", help="HuggingFace model path or local path")
    parser.add_argument("--dataset", required=True, help="HuggingFace dataset name")
    parser.add_argument("--config", default=None, help="Dataset config (e.g. python, cpp, java)")
    parser.add_argument("--split", default="train", help="Dataset split (default: train)")
    parser.add_argument("--output", required=True, help="Output directory for results")
    parser.add_argument("--max-length", type=int, default=4096, help="Max sequence length")
    parser.add_argument("--batch-size", type=int, default=8, help="Batch size for scoring")
    parser.add_argument(
        "--use-system-prompt",
        action="store_true",
        default=False,
        help="Prepend the Functional Correctness system prompt to each conversation",
    )
    args = parser.parse_args()

    scorer = RewardModelScorer(args.model_path, args.max_length, args.use_system_prompt)

    results = evaluate(
        scorer=scorer,
        dataset_name=args.dataset,
        config=args.config,
        split=args.split,
        batch_size=args.batch_size,
    )

    print_results(results)
    save_results(results, args.output)


if __name__ == "__main__":
    main()