"""Stochastic system-prompt constructor for preference training data.

Provides a HuggingFace ``map`` function that assigns each training example one
of three system-prompt treatments (none, aspect-specific, or full criteria) with
configurable probabilities, then tokenises the resulting conversations.
"""

import random


aspect_map = {
    0: "Readability and Maintainability",
    1: "Runtime Efficiency",
    2: "Security Hardness",
    3: "Functional Correctness",
    4: "Memory Efficiency",
    5: "Helpfulness",
    6: "Harmlessness"
}


def system_prompt_map(
        example,
        tokenizer,
        no_criteria_cumulative_probability=0.15,
        all_criteria_cumulative_probability=0.75,
    ):
    """
    Map function for Hugging Face's `datasets` library to create a dynamic system prompt for the preference dataset.
    """

    # Probability value check
    if not (0 <= no_criteria_cumulative_probability <= 1) and not (0 <= all_criteria_cumulative_probability <= 1):
        raise ValueError("Cumulative probabilities must be between 0 and 1.")
    if no_criteria_cumulative_probability >= all_criteria_cumulative_probability:
        raise ValueError("Cumulative probability for no criteria must be less than the cumulative probability for all criteria.")
    
    # Randomly select a cumulative probability for the criteria
    cumulative_probability = random.uniform(0, 1)

    # Extract the axis of evaluation from the example. Can be one of 'Helpfulness', 'Harmlessness', 'Readability and Maintainability', 'Runtime Efficiency', 'Security Hardness', 'Functional Correctness' or 'Memory Efficiency'
    axis_of_evaluation = aspect_map[example["aspect"]]

    # Reward model base system prompt
    base_system_prompt = f"""You are an experienced AI judge and evaluator, specifically designed and trained to assess the overall quality of responses to human prompts. The prompts can be of various types, including but not limited to open-ended questions, coding tasks, creative writing, requests for information, and more. The responses may be generated by a language model or a human and may include text, code, or both. You will receive the prompt and the generated response for each judgement session. Your exact mandate is to score the response based on a pre-specified but flexible set of criteria. 

The following criteria must govern your scoring for the current judgment session:

1. **Helpfulness**: How well does the response address the prompt? Does it provide a complete and thorough answer, or is it lacking in essential details? This is a tenet that applies to evaluating all types of responses. You must score helpful responses higher than less unhelpful ones.

2. **Harmlessness**: Does the response contain any harmful or inappropriate content? This includes hate speech, violence, self-harm, and other forms of harmful content. This is a tenet that applies to evaluating all types of responses. You must score harmless responses higher than harmful ones."""

    # Readability and Maintainability prompt
    readability_prompt = f"""**Readability and Maintainability**: Does the response follow best practices for readability and maintainability? Examples include using clear and descriptive names, following consistent formatting and style guidelines, modularizing code into functions or classes, and providing comments and documentation where necessary, among others. This is a tenet that applies to evaluating code responses. You must score more readable and maintainable responses higher than less readable and maintainable ones."""

    # Runtime Efficiency prompt
    runtime_prompt = f"""**Runtime Efficiency**: Does the response follow best practices for runtime efficiency? Examples include using efficient algorithms and data structures, minimizing time complexity, avoiding unnecessary computations, caching results, and leveraging parallel processing or asynchronous programming techniques where appropriate, among others. This is a tenet that applies to evaluating code responses. You must score more runtime-efficient responses higher than less runtime-efficient ones."""

    # Memory Efficiency prompt
    memory_prompt = f"""**Memory Efficiency**: Does the response follow best practices for memory efficiency? Examples include using efficient data structures, minimizing memory usage, avoiding memory leaks, and pooling/managing resources effectively, among others. This is a tenet that applies to evaluating code responses. You must score more memory-efficient responses higher than less memory-efficient ones."""

    # Security Hardness prompt
    security_prompt = f"""**Security Hardness**: Does the response follow best practices for security hardness? Examples include input validation, output encoding, proper error handling, and secure coding practices, among others. This is a tenet that applies to evaluating code responses. You must score more secure and less vulnerable responses higher than less secure and more vulnerable ones."""
    
    # Functional Correctness prompt
    functional_prompt = f"""**Functional Correctness**: Does the response follow best practices for functional correctness? Examples include correct implementation of algorithms, adherence to specifications, and proper handling of edge cases, among others. This is a tenet that applies to evaluating code responses. You must score more functionally correct responses higher than less functionally correct ones."""

    # Construct the system prompt based on the cumulative probability
    if cumulative_probability < no_criteria_cumulative_probability:
        example["system"] = None
    elif cumulative_probability < all_criteria_cumulative_probability:
        if axis_of_evaluation == "Readability and Maintainability":
            example["system"] = f"{base_system_prompt}\n\n3. {readability_prompt}\n\n"
        elif axis_of_evaluation == "Runtime Efficiency":
            example["system"] = f"{base_system_prompt}\n\n3. {runtime_prompt}\n\n"
        elif axis_of_evaluation == "Memory Efficiency":
            example["system"] = f"{base_system_prompt}\n\n3. {memory_prompt}\n\n"
        elif axis_of_evaluation == "Security Hardness":
            example["system"] = f"{base_system_prompt}\n\n3. {security_prompt}\n\n"
        elif axis_of_evaluation == "Functional Correctness":
            example["system"] = f"{base_system_prompt}\n\n3. {functional_prompt}\n\n"
        elif axis_of_evaluation == "Helpfulness" or axis_of_evaluation == "Harmlessness":
            # For Helpfulness and Harmlessness, we don't add any specific criteria
            example["system"] = f"{base_system_prompt}\n\n"
        else:
            raise ValueError(f"Unknown axis of evaluation: {axis_of_evaluation}")
    else:
        # Construct the optional criteria prompt in a random order with numbering
        criteria_prompts = [readability_prompt, runtime_prompt, memory_prompt, security_prompt, functional_prompt]
        random.shuffle(criteria_prompts)
        criteria_prompts = [f"{i + 3}. {prompt}" for i, prompt in enumerate(criteria_prompts)]
        optional_criteria_prompts = "\n\n".join(criteria_prompts)
        example["system"] = f"{base_system_prompt}\n\n{optional_criteria_prompts}\n\n"

    # Create chosen and rejected conversational dicts
    if example["system"] is not None:
        example["Chosen_Conversation"] = [
            {"role": "system", "content": example["system"]},
            {"role": "user", "content": example["input"]},
            {"role": "assistant", "content": example["chosen"]},
        ]
        example["Rejected_Conversation"] = [
            {"role": "system", "content": example["system"]},
            {"role": "user", "content": example["input"]},
            {"role": "assistant", "content": example["rejected"]},
        ]
    else:
        example["Chosen_Conversation"] = [
            {"role": "user", "content": example["input"]},
            {"role": "assistant", "content": example["chosen"]},
        ]
        example["Rejected_Conversation"] = [
            {"role": "user", "content": example["input"]},
            {"role": "assistant", "content": example["rejected"]},
        ]
    
    # Create reduced chosen and rejected conversations without system prompt
    example["Chosen_Conversation_Reduced"] = [
        {"role": "user", "content": example["input"]},
        {"role": "assistant", "content": example["chosen"]},
    ]
    example["Rejected_Conversation_Reduced"] = [
        {"role": "user", "content": example["input"]},
        {"role": "assistant", "content": example["rejected"]},
    ]

    # Record the sequence lengths of the conversations and the reduced conversations after applying the chat template
    example["Chosen_Conversation_Length"] = len(
        tokenizer.apply_chat_template(
            example["Chosen_Conversation"], 
            tokenize=True,
            add_generation_prompt=False,
            enable_thinking=False
        )
    )
    example["Rejected_Conversation_Length"] = len(
        tokenizer.apply_chat_template(
            example["Rejected_Conversation"], 
            tokenize=True,
            add_generation_prompt=False,
            enable_thinking=False
        )
    )
    example["Chosen_Conversation_Reduced_Length"] = len(
        tokenizer.apply_chat_template(
            example["Chosen_Conversation_Reduced"], 
            tokenize=True,
            add_generation_prompt=False,
            enable_thinking=False
        )
    )
    example["Rejected_Conversation_Reduced_Length"] = len(
        tokenizer.apply_chat_template(
            example["Rejected_Conversation_Reduced"], 
            tokenize=True,
            add_generation_prompt=False,
            enable_thinking=False
        )
    )

    return example